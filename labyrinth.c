void input_handler(Player *p, Game *g){
    char ch = getch();

    if(ch == 'q'){
        g->running = false;
    }
    else if(ch == 'r'){
        p->y = 1;
        p->x = 1;
    }
    else if(ch == 'w'){
        if(g->map[p->y -1 ][p->x] == 0){
            p->y --;
        }
    }
    else if(ch == 'a'){
        if(g->map[p->y][p->x - 1] == 0){
            p->x --;
        }
    }
    else if(ch == 's'){
        if(g->map[p->y + 1][p->x] == 0){
            p->y ++;
        }
    }
    else if(ch == 'd'){
        if(g->map[p->y][p->x + 1] == 0){
            p->x ++;
        }
    }
}

void main_loop (Player *p, Game *g){
    while(g->running == true){
        for(int i = 0; i <= ROWS - 1; i++){
            for(int j = 0; j <= COLS - 1; j++){
                if (i == p->y && j == p->x){
                    printw("<>");
                }
                else if(g->map[i][j] == 0){
                    printw("  ");
                }
                else if(g->map[i][j] == 1){
                    printw("[]");
                }
            }
            printw("\n");
        }

        refresh();
        input_handler(p, g);
        erase();
    }
}

