int running = 1;
int player_x = 1;
int player_y = 1;

void input_handler(){
    char ch = getch();

    if(ch == 'q'){
        running = 0;
    }
    else if(ch == 'r'){
        player_y = 1;
        player_x = 1;
    }
    else if(ch == 'w'){
        if(map[player_y -1 ][player_x] == 0){
            player_y --;
        }
    }
    else if(ch == 'a'){
        if(map[player_y][player_x - 1] == 0){
            player_x --;
        }
    }
    else if(ch == 's'){
        if(map[player_y + 1][player_x] == 0){
            player_y ++;
        }
    }
    else if(ch == 'd'){
        if(map[player_y][player_x + 1] == 0){
            player_x ++;
        }
    }
}

int main(){
    initscr();
    raw();
    curs_set(0);
    noecho();

    while(running == 1){
        for(int i = 0; i <= ROWS - 1; i++){
            for(int j = 0; j <= COLS - 1; j++){
                if (i == player_y && j == player_x){
                    printw("*");
                }
                else if(map[i][j] == 0){
                    printw(" ");
                }
                else if(map[i][j] == 1){
                    printw("#");
                }
            }
            printw("\n");
        }

        refresh();
        input_handler();
        clear();
    }

    endwin();
    return 0;
}
